from __future__ import annotations

import statistics
import tempfile
import time
import tracemalloc
from collections.abc import Callable
from dataclasses import dataclass
from pathlib import Path

import pytest

from gummysnake.drawing.renderer3d import Model3D
from gummysnake.drawing.software3d import save_obj, save_stl, sphere_model, torus_model
from gummysnake.drawing.software3d.math3d import triangle_normal

MEMORY_BUDGET_MIB = 4.0
REPEATS = 3


@dataclass(frozen=True)
class ExportMeasurement:
    mean_ms: float
    min_ms: float
    max_ms: float
    peak_mib: float
    output_bytes: int


def _legacy_save_obj(model: Model3D, path: Path) -> Path:
    lines: list[str] = ["# Generated by Gummy Snake"]
    vertex_offset = 1
    for mesh in model.meshes:
        for vertex in mesh.vertices:
            lines.append(f"v {vertex.x:.9g} {vertex.y:.9g} {vertex.z:.9g}")
        for u, v in mesh.texcoords:
            lines.append(f"vt {u:.9g} {v:.9g}")
        for face in mesh.faces:
            lines.append("f " + " ".join(str(index + vertex_offset) for index in face))
        vertex_offset += len(mesh.vertices)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return path


def _legacy_save_stl(model: Model3D, path: Path, *, name: str = "gummy_snake_model") -> Path:
    lines = [f"solid {name}"]
    for mesh in model.meshes:
        for face in mesh.faces:
            if len(face) < 3:
                continue
            first = face[0]
            for index in range(1, len(face) - 1):
                a, b, c = (
                    mesh.vertices[first],
                    mesh.vertices[face[index]],
                    mesh.vertices[face[index + 1]],
                )
                normal = triangle_normal(a, b, c)
                lines.append(f"  facet normal {normal.x:.9g} {normal.y:.9g} {normal.z:.9g}")
                lines.append("    outer loop")
                for vertex in (a, b, c):
                    lines.append(f"      vertex {vertex.x:.9g} {vertex.y:.9g} {vertex.z:.9g}")
                lines.append("    endloop")
                lines.append("  endfacet")
    lines.append(f"endsolid {name}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return path


def _measure_export(callback: Callable[[Path], Path]) -> ExportMeasurement:
    timings: list[float] = []
    peaks: list[float] = []
    output_bytes = 0
    with tempfile.TemporaryDirectory() as directory:
        root = Path(directory)
        for index in range(REPEATS):
            tracemalloc.start()
            start = time.perf_counter_ns()
            output = callback(root / f"export_{index}")
            timings.append((time.perf_counter_ns() - start) / 1_000_000)
            _, peak = tracemalloc.get_traced_memory()
            tracemalloc.stop()
            peaks.append(peak / 1024 / 1024)
            output_bytes = output.stat().st_size
            output.unlink()
    return ExportMeasurement(
        mean_ms=statistics.mean(timings),
        min_ms=min(timings),
        max_ms=max(timings),
        peak_mib=max(peaks),
        output_bytes=output_bytes,
    )


@pytest.mark.benchmark
def test_large_model_export_memory_budget_benchmark() -> None:
    obj_model = torus_model(100, 20, detail_x=192, detail_y=96)
    stl_model = sphere_model(100, detail_x=192, detail_y=128)
    cases = {
        "obj_torus_192x96": (
            lambda path: save_obj(obj_model, path.with_suffix(".obj")),
            lambda path: _legacy_save_obj(obj_model, path.with_suffix(".obj")),
        ),
        "stl_sphere_192x128": (
            lambda path: save_stl(stl_model, path.with_suffix(".stl")),
            lambda path: _legacy_save_stl(stl_model, path.with_suffix(".stl")),
        ),
    }

    for name, (streaming, legacy) in cases.items():
        streaming_result = _measure_export(streaming)
        legacy_result = _measure_export(legacy)
        streaming_fits_budget = streaming_result.peak_mib <= MEMORY_BUDGET_MIB
        legacy_fits_budget = legacy_result.peak_mib <= MEMORY_BUDGET_MIB
        effective_winner = (
            "streaming" if streaming_fits_budget and not legacy_fits_budget else "timing"
        )
        print(
            f"large_model_export {name} streaming: mean_ms={streaming_result.mean_ms:.2f} "
            f"min_ms={streaming_result.min_ms:.2f} max_ms={streaming_result.max_ms:.2f} "
            f"peak_mib={streaming_result.peak_mib:.2f} "
            f"output_bytes={streaming_result.output_bytes} "
            f"fits_{MEMORY_BUDGET_MIB:.0f}mib_budget={streaming_fits_budget}"
        )
        print(
            f"large_model_export {name} legacy_list: mean_ms={legacy_result.mean_ms:.2f} "
            f"min_ms={legacy_result.min_ms:.2f} max_ms={legacy_result.max_ms:.2f} "
            f"peak_mib={legacy_result.peak_mib:.2f} output_bytes={legacy_result.output_bytes} "
            f"fits_{MEMORY_BUDGET_MIB:.0f}mib_budget={legacy_fits_budget}"
        )
        print(
            f"large_model_export {name} comparison: "
            f"wall_time_ratio_legacy_over_streaming="
            f"{legacy_result.mean_ms / max(streaming_result.mean_ms, 1e-9):.2f}x "
            f"peak_memory_ratio_legacy_over_streaming="
            f"{legacy_result.peak_mib / max(streaming_result.peak_mib, 1e-9):.2f}x "
            f"effective_winner_under_budget={effective_winner}"
        )
        assert streaming_result.output_bytes > 0
        assert legacy_result.output_bytes > 0
        size_delta = abs(streaming_result.output_bytes - legacy_result.output_bytes)
        assert size_delta / max(legacy_result.output_bytes, 1) <= 0.05
        assert streaming_fits_budget
        assert legacy_result.peak_mib > streaming_result.peak_mib
